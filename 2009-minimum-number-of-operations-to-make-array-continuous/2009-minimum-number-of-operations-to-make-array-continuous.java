class Solution {
    public int minOperations(int[] nums) {
        int n = nums.length;
        Arrays.sort(nums); 
        int m = 1;
        for (int i = 1; i < n; i++) 
            if (nums[i] != nums[i - 1]) 
                nums[m++] = nums[i];
        int ans = n;
        for (int i = 0, j = 0; i < m; i++) {
            while (j < m && nums[j] - nums[i] <= n - 1) 
                j++;
            ans = Math.min(ans, n - (j - i));
            if(ans < i || ans==0 )
                break;
        }
        return ans;
    }
}